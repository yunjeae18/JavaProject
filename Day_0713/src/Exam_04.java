//반복문: for(), while(), do~while() 조건식이 참이면 반복
//


public class Exam_04 {

	public static void main(String[] args) {
    int i;
    for(i=1; i<=5; i++);{
       System.out.println("홍길동");
      
	}
   System.out.println("i="+i);
   
   int j;
   for(j=0; j<5; j+=2) {
	   System.out.println(j);
   }
   System.out.println(j);
   
 
}
}